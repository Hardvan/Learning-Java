
public class SwitchCase {
    public static void main(String[] args) {

        String str = "Hardvan";

        // ? We can use switch case with Strings in Java
        switch (str) {
            case "Hardvan":
                System.out.println("Hello Hardvan");
                break;
            case "Hardik":
                System.out.println("Hello Hardik");
                break;
            default:
                System.out.println("Hello World");
        }

    }
}
